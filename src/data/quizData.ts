// Helper function to shuffle array
export function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Get random questions for pre-valentine quiz
export function getPreValentineQuestions(count: number): Question[] {
  const allQuestions = [...thaiBLQuestions, ...gameChangersQuestions, ...strayKidsQuestions];
  const shuffled = shuffleArray(allQuestions);
  return shuffled.slice(0, count).map(q => ({
    ...q,
    options: shuffleArray(q.options),
  }));
}

// Get couples quiz questions with shuffled options
export function getCouplesQuizQuestions(): Question[] {
  return couplesQuizQuestions.map(q => ({
    ...q,
    options: shuffleArray(q.options),
  }));
}

